/* Create tables */

CREATE TABLE person (id INTEGER PRIMARY KEY, name TEXT, residence TEXT, age INTEGER);

CREATE TABLE flight (id INTEGER PRIMARY KEY, person_id INTEGER, name TEXT, start_city TEXT, dest_city TEXT, date DATE, FOREIGN KEY (person_id) REFERENCES person(id));

CREATE TABLE phone_contract (id INTEGER PRIMARY KEY, person_id INTEGER, name TEXT, phone_number INTEGER, status TEXT, FOREIGN KEY (person_id) REFERENCES person(id));

CREATE TABLE messages (id INTEGER PRIMARY KEY, contract_sender_id INTEGER, contract_receiver_id INTEGER, message TEXT, sent DATE, FOREIGN KEY (contract_sender_id) REFERENCES phone_contract(id), FOREIGN KEY (contract_receiver_id) REFERENCES phone_contract(id));

CREATE TABLE bank_transaction (bank_transaction_pk INTEGER PRIMARY KEY, account_number_fk INTEGER,date DATE, description TEXT, amount REAL, FOREIGN KEY (account_number_fk) REFERENCES account (account_number_pk));

CREATE TABLE account (account_number_pk INTEGER PRIMARY KEY, name TEXT, city TEXT);

